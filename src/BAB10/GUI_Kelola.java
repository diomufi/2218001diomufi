    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try {
        koneksi(); // Membuat koneksi ke database

        // Menyiapkan pernyataan SQL untuk pencarian berdasarkan ID_User, Nama, dan Alamat
        String searchSql = "SELECT * FROM Data_nasabah WHERE ID_User LIKE ? OR Nama LIKE ? OR Alamat LIKE ?";
        PreparedStatement searchStatement = conn.prepareStatement(searchSql);

        // Mengatur parameter untuk pencarian dengan menggunakan placeholder LIKE
        searchStatement.setString(1, "%" + txtSearch.getText() + "%"); // ID_User
        searchStatement.setString(2, "%" + txtSearch.getText() + "%"); // Nama
        searchStatement.setString(3, "%" + txtSearch.getText() + "%"); // Alamat

        // Jalankan kueri
        ResultSet resultSet = searchStatement.executeQuery();

        // Hapus tabel
        DefaultTableModel dataModel = (DefaultTableModel) tbm.getModel();
        dataModel.setRowCount(0);

        // Tampilkan hasil pencarian dalam tabel
        boolean found = false;
        while (resultSet.next()) {
            found = true;
            List<Object> list = new ArrayList<>();
            list.add(resultSet.getString("ID_User"));
            list.add(resultSet.getString("Nama"));
            list.add(resultSet.getString("Jenis_Kelamin"));
            list.add(resultSet.getString("Alamat"));
            list.add("Saldo Awal");
            list.add(resultSet.getDouble("Saldo"));
            list.add(0.0);
            list.add(resultSet.getDouble("Saldo"));

            dataModel.addRow(list.toArray());
        }

        // Tutup result set dan pernyataan
        resultSet.close();
        searchStatement.close();
        
         txtSearch.setText("");
        // Jika tidak ada data ditemukan, tampilkan JOptionPane
        if (!found) {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan.", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }

    } catch (SQLException e) {
        // Tangani pengecualian SQL
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSearchActionPerformed

