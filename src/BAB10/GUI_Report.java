    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try {
        koneksi(); // Membuat koneksi ke database
        String keyword = txtSearch.getText();

        // Membuat objek PreparedStatement untuk mencari data berdasarkan nama atau ID pengguna
        String query = "SELECT p.User_ID, dn.Nama, p.Pengeluaran, p.Harga " +
                       "FROM pengeluaran p " +
                       "JOIN data_nasabah dn ON p.User_ID = dn.ID_User " +
                       "WHERE dn.Nama LIKE ? OR p.User_ID LIKE ?";
        try (PreparedStatement pstmt = conn.prepareStatement(query)) {
            pstmt.setString(1, "%" + keyword + "%");
            pstmt.setString(2, "%" + keyword + "%");

            try (ResultSet resultSet = pstmt.executeQuery()) {
                // Membersihkan tabel sebelum menampilkan hasil pencarian
                DefaultTableModel dataModel = (DefaultTableModel) TabelCatatan.getModel();
                int rowCount = dataModel.getRowCount();
                while (rowCount > 0) {
                    dataModel.removeRow(rowCount - 1);
                    rowCount = dataModel.getRowCount();
                }

                // Menampilkan hasil pencarian di tabel
                while (resultSet.next()) {
                    Object[] rowData = {
                        resultSet.getString("User_ID"),
                        resultSet.getString("Nama"),
                        resultSet.getString("Pengeluaran"),
                        resultSet.getDouble("Harga")
                    };
                    dataModel.addRow(rowData);
                }
            }
        }
    } catch (SQLException e) {
        // Menampilkan pesan error jika terjadi kesalahan
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
       
    }//GEN-LAST:event_btnSearchActionPerformed

