    private void btn_tambahsaldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahsaldoActionPerformed

        // TODO add your handling code here:
        DefaultTableModel dataModel = (DefaultTableModel) tbm.getModel();
int row = tbm.getSelectedRow();

try {
    koneksi(); // Membuat koneksi ke database

    if (row == -1) {
        JOptionPane.showMessageDialog(null, "Pilih baris terlebih dahulu.", "Error", JOptionPane.ERROR_MESSAGE);
        throw new IllegalArgumentException("Pilih baris terlebih dahulu.");
    }

    // Mendapatkan data dari input pengguna
    String idUser = (String) dataModel.getValueAt(row, 0);
    double saldoAwal = (double) dataModel.getValueAt(row, 5);
    double tambah = Double.parseDouble(txt_tambahsaldo.getText());

    // Update saldo di database
    String updateSql = "UPDATE Data_nasabah SET Saldo = ? WHERE ID_User = ?";
    PreparedStatement updateStatement = conn.prepareStatement(updateSql);
    updateStatement.setDouble(1, saldoAwal + tambah);
    updateStatement.setString(2, idUser);
    updateStatement.executeUpdate();

    // Update saldo di tabel
    double saldoAkhir = saldoAwal + tambah;
    dataModel.setValueAt("Tambah", row, 4);
    dataModel.setValueAt(tambah, row, 6);
    dataModel.setValueAt(saldoAkhir, row, 7);

    clear();
    JOptionPane.showMessageDialog(this, "Saldo berhasil ditambahkan.", "Informasi", JOptionPane.INFORMATION_MESSAGE);

    // Setel nilai JTextField kembali ke 0
    txt_tambahsaldo.setText("0");
} catch (SQLException | NumberFormatException e) {
    // Tangani exception dengan menampilkan pesan menggunakan JOptionPane
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}

        
    }//GEN-LAST:event_btn_tambahsaldoActionPerformed

