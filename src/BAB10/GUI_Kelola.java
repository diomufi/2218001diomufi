    private void btn_cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cetakActionPerformed
     // TODO add your handling code here:
     DefaultTableModel dataModel = (DefaultTableModel) tbm.getModel();

try {
    koneksi(); // Membuat koneksi ke database

    // Menyiapkan pernyataan SQL untuk menyisipkan data ke database
    String insertSql = "INSERT INTO Data_nasabah (ID_User, Nama, Jenis_Kelamin, Alamat, Saldo) VALUES (?, ?, ?, ?, ?)";
    PreparedStatement preparedStatement = conn.prepareStatement(insertSql);

    // Mendapatkan data dari input pengguna
    String idUser = txt_id.getText();
    String nama = txt_nama.getText();
    String jenisKelamin = cmb_jenkel.getSelectedItem().toString();
    String alamat = txt_alamat.getText();
    double saldo = Double.parseDouble(txt_saldoawal.getText());

    // Menjalankan pernyataan SQL untuk memeriksa apakah ID_User sudah ada
    String checkIdSql = "SELECT COUNT(*) FROM Data_nasabah WHERE ID_User = ?";
    PreparedStatement checkIdStatement = conn.prepareStatement(checkIdSql);
    checkIdStatement.setString(1, idUser);

    ResultSet resultSet = checkIdStatement.executeQuery();
    resultSet.next();
    int idCount = resultSet.getInt(1);

    // Periksa apakah ID_User sudah ada di database
    if (idCount > 0) {
        JOptionPane.showMessageDialog(this, "ID_User sudah ada. Pilih ID_User yang unik.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; // Hentikan eksekusi jika ID_User sudah ada
    }

    // Periksa apakah semua input sudah diisi
    if (idUser.isEmpty() || nama.isEmpty() || jenisKelamin.isEmpty() || alamat.isEmpty() || txt_saldoawal.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Isi semua kolom dengan benar.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; // Hentikan eksekusi jika ada yang kosong
    }

    // Mengatur parameter pada pernyataan SQL
    preparedStatement.setString(1, idUser);
    preparedStatement.setString(2, nama);
    preparedStatement.setString(3, jenisKelamin);
    preparedStatement.setString(4, alamat);
    preparedStatement.setDouble(5, saldo);

    // Menjalankan pernyataan SQL untuk menyisipkan data
    int affectedRows = preparedStatement.executeUpdate();

    if (affectedRows > 0) {
        // Menambahkan data ke tabel
        List<Object> list = new ArrayList<>();
        list.add(idUser);
        list.add(nama);
        list.add(jenisKelamin);
        list.add(alamat);
        list.add("Saldo Awal");
        list.add(saldo);
        list.add(0.0);
        list.add(saldo);

        dataModel.addRow(list.toArray());

        // Membersihkan input setelah berhasil dimasukkan ke database
        clear();

        JOptionPane.showMessageDialog(this, "Data berhasil disimpan ke database", "Informasi", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data ke database", "Error", JOptionPane.ERROR_MESSAGE);
    }

} catch (SQLException | NumberFormatException e) {
    // Tangani exception dengan menampilkan pesan menggunakan JOptionPane
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}

    }//GEN-LAST:event_btn_cetakActionPerformed

